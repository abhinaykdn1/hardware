void lcd_init(void)
{
	SETP(LCD_RST);
	SETD(LCD_RST);

	SETD(LCD_CE);
	SETD(LCD_DC);
	SETD(LCD_DIN);
	SETD(LCD_CLK);

	_delay_ms(15);
	CLRP(LCD_RST);
	_delay_ms(64);
	SETP(LCD_RST);
	SETP(LCD_CE);

    lcd_send_cmd(0x21);  // LCD Extended Commands
    lcd_send_cmd(0xC8);  // Set LCD Vop(Contrast)
    lcd_send_cmd(0x06);  // Set Temp coefficent
    lcd_send_cmd(0x13);  // LCD bias mode 1:48
    lcd_send_cmd(0x20);  // Standard Commands, Horizontal addressing
    lcd_send_cmd(0x0C);  // LCD in normal mode

    lcd_clear();

	int contrast = 0x3A;
	lcd_send_cmd(0x21);				// LCD Extended Commands
	lcd_send_cmd(0x80 | contrast);	// Set LCD Vop(Contrast)
	lcd_send_cmd(0x20);				// LCD std cmds, hori addr mode

}


