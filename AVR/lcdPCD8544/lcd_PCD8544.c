unsigned char text_buf[LCD_CHARS];

unsigned char chr_pos = 0;
unsigned int curr_addr = 0;

void lcd_send_cmd(unsigned char data)
{
	CLRP(LCD_CE);
	CLRP(LCD_DC); //0 for command, 1 for data
	for (unsigned char i=0; i<8; i++) {
		if ((data>>(7-i)) & 0x01) {
			SETP(LCD_DIN);
		} else {
			CLRP(LCD_DIN);
		}

		// Toggle the clock
		CLRP(LCD_CLK);
		SETP(LCD_CLK);
	}
	SETP(LCD_CE);
}

void lcd_send_data(unsigned char data)
{
	CLRP(LCD_CE);
	SETP(LCD_DC); //0 for command, 1 for data
	for (unsigned char i=0; i<8; i++) {
		if ((data>>(7-i)) & 0x01) {
			SETP(LCD_DIN);
		} else {
			CLRP(LCD_DIN);
		}

		// Toggle the clock
		CLRP(LCD_CLK);
		SETP(LCD_CLK);
	}
	SETP(LCD_CE);
}

void lcd_base_addr(unsigned int addr) {
	lcd_send_cmd(0x80 |(addr % LCD_WIDTH));
	lcd_send_cmd(0x40 |(addr / LCD_WIDTH));
}

void lcd_clear(void)
{
	int lcd_size =  ((LCD_WIDTH * LCD_HEIGHT) / CHAR_HEIGHT);

    for (int i=0; i<lcd_size; i++) {
    	lcd_send_data(0x00);
    }
}

void lcd_fill(void)
{
	int lcd_size =  ((LCD_WIDTH * LCD_HEIGHT) / CHAR_HEIGHT);

    for (int i=0; i<lcd_size; i++) {
    	lcd_send_data(0xFF);
    }
}

