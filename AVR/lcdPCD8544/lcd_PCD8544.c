void lcd_chr(unsigned char chr)
{
	char out_char = chr>31?(chr<128?chr:32):32;
	lcd_base_addr(curr_addr);
	for(unsigned char i=0; i < CHAR_WIDTH - 1; i++) {
		lcd_send_data(pgm_read_byte(&font5x7[out_char-32][i]) << 1);
		curr_addr++;
	}
	lcd_send_data(0);
	curr_addr++;
	curr_addr = curr_addr % LCD_BYTES;
}

void lcd_refresh_text()
{
	curr_addr = 0;
	for (unsigned char i = 0; i < LCD_CHARS; i++) {
		lcd_chr(text_buf[i]);
	}
}

void lcd_char_scroll()
{
	for (unsigned char i = 0; i < LCD_CHARS - LCD_COLS; i++) {
		text_buf[i] = text_buf[i + LCD_COLS];
	}
	for (unsigned char i = LCD_CHARS - LCD_COLS; i < LCD_CHARS; i++) {
		text_buf[i] = 0;
	}
	lcd_refresh_text();
	chr_pos = LCD_CHARS - LCD_COLS;
}

void lcd_putchr(unsigned char chr)
{
	curr_addr = chr_pos * CHAR_WIDTH;
	if (chr != '\n') {
		text_buf[chr_pos++] = chr;
		lcd_chr(chr);
	} else {
		lcd_chr(text_buf[chr_pos]);
		unsigned char new_chr_pos = chr_pos - (chr_pos % LCD_COLS) + LCD_COLS;
		for (; chr_pos<new_chr_pos; chr_pos++) {
			text_buf[chr_pos] = 0;
		}
	}
	if (chr_pos>=LCD_CHARS) {
		lcd_char_scroll();
	}
	curr_addr = chr_pos * CHAR_WIDTH;
	lcd_chr(127);
//	chr_pos = chr_pos % LCD_CHARS;
}

void lcd_str(char *str)
{
    while (*str) {
    	lcd_putchr(*str++);
//    	_delay_ms(50)
    }
}

